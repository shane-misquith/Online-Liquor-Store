    private void delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_buttonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) cart_tables_user.getModel();
        
            try {
                int SelectedRowIndex = cart_tables_user.getSelectedRow();
                
                String pro_name =model.getValueAt(SelectedRowIndex,0).toString().trim();
                int quan = Integer.parseInt(model.getValueAt(SelectedRowIndex,2).toString().trim());
                Class.forName("oracle.jdbc.OracleDriver");
                Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "Project", "shane95");
                
                Statement stat=con.createStatement();
                stat.executeUpdate("delete from cart_table where product_name='"+pro_name+"' and username = '"+user+"' ");
                stat.executeUpdate("update product_list set stock = stock + '"+quan+"' where product_name = '"+pro_name+"' ");
                this.dispose();
                Cart p = new Cart(user);
                p.setVisible(true);
                JOptionPane.showMessageDialog(null,"Item removed");
                model.removeRow(SelectedRowIndex);
                
                con.close();
            } 
            catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null,"Select a product to delete");
                Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        
    }//GEN-LAST:event_delete_buttonActionPerformed

