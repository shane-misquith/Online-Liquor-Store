    public Cart(String user){
        initComponents();
        this.user=user;
        
        DefaultTableModel model = (DefaultTableModel) cart_tables_user.getModel();
        deleteAllRows(model);
        try {
                    Class.forName("oracle.jdbc.OracleDriver");
                    Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "Project", "shane95");                    
                    String query="select product_name,quantity,price,pro_type from cart_table where username='"+user+"' ";
                    PreparedStatement pre=con.prepareStatement(query);
                    CallableStatement cst=con.prepareCall("{call total(?,?)");
                    ResultSet re=pre.executeQuery();
                    while(re.next()){
                        String pro_name = re.getString("product_name");
                        String quan = re.getString("quantity");
                        String price = re.getString("price");
                        String type = re.getString("pro_type");
                        cst.setString(1, user);
                        cst.registerOutParameter(2, Types.VARCHAR);
                        cst.execute();
                        int total_price = cst.getInt(2);
                        model.addRow(new Object[]{pro_name,type,quan,price});
                        total_amt.setText(String.valueOf(total_price));
                    }
        }catch(ClassNotFoundException | SQLException ex){
                    JOptionPane.showMessageDialog(null,"User does not exist");
                    Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
          
        
    }

